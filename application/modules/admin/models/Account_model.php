        }
    }

    function type_chart_list()
    {
        $this->db->from('ref_charttypes');
        $this->db->order_by('id','asc');
        $result = $this->db->get();

        $return = array('');

        if ($result->num_rows() > 0)
        {
            $return[0] = '-- Pilih Tipe Rekening --';
            foreach ($result->result_array() as $row) {
                $return[$row['id']] = $row['type_name'];
            }
        } else {
            $return[0] = '-- Pilih Tipe Rekening --';
        }

        return $return;     
    }  

    function account_list()
