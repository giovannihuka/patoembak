        $return = array('');

        if ($result->num_rows() > 0)
        {
            $return[0] = '-- Pilih Rekening Induk --';
            foreach ($result->result_array() as $row) {
                $return[$row['id']] = $row['chart_name'];
            }
        } else {
            $return[0] = '-- Pilih Rekening Induk --';
        }

        return $return;     
    }  

